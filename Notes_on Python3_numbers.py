# You can add (+) subtract (-) multiply (*) and divide (/) integers in Python

print(2+3)
print(2-3)
print(2*3)
print(2/3)

# Two multiplacation symbols = an exponent
print(3**2)
print(3**3)
print(10**6)

# python also supports order of operations using PEMDAS

print(2+3*4)
print((2+3)*4)

# Python also supports floats or decimal numbers. 
print(0.1+0.1)
print(2*0.2)

#integers and floats. When you divide any two numbers you always get a float.
print(4/2)

# If you mix floats and integers you wil also get a float. 
print(2+3.0)
print(2*3.0)
print(2.0**3)

# When working with large numbers you can add underscores to make them more readable
universe_age = 14_000_000_000
print(universe_age)

